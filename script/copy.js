//----------- copy button functionality ------------
const copyAmount = document.getElementById('copy-amount');

// copy button 1
document.getElementById("copy-btn-1").addEventListener("click", function () {
  const textToCopy = document.getElementById("copy-txt-1").innerText;

  navigator.clipboard
    .writeText(textToCopy)
    .then(() => {
      alert("নম্বর কপি হয়েছে: " + textToCopy);

      let currentCopy = Number(copyAmount.innerText);
      copyAmount.innerText = currentCopy + 1;
    })
});


